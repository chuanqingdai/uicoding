function localizeLabel(value, group) {
  return lessonLabelEn[group]?.[value] || value;
}

function localizeDuration(value) {
  return typeof value === 'string' ? value.replace('分钟', 'min') : value;
}

function getLessonTitleEn(lesson) {
  return lessonCopyEn[lesson.id]?.title || lesson.originalTitle || lesson.title;
}

function buildLessonFallbackDescription(lesson) {
  const originalTitle = lesson.originalTitle?.trim();
  const sourceName = lesson.sourceName?.trim();
  const category = localizeLabel(lesson.category, 'category');
  const primaryTool = localizeLabel(lesson.tools?.[0], 'tools');

  if (lesson.description && !/[\u4e00-\u9fff]/.test(lesson.description)) {
    return lesson.description;
  }

  if (originalTitle) {
    return sourceName
      ? `A Uicoding reading note based on ${sourceName}, centered on "${originalTitle}" and the practical workflow behind it.`
      : `A Uicoding reading note centered on "${originalTitle}" and the practical workflow behind it.`;
  }

  const parts = [category, primaryTool].filter(Boolean).join(' and ');

  if (sourceName && parts) {
    return `A Uicoding reading note based on ${sourceName}, focused on ${parts}.`;
  }

  if (sourceName) {
    return `A Uicoding reading note based on ${sourceName}.`;
  }

  return 'A Uicoding learning note for beginners exploring practical AI coding workflows.';
}

function getLessonDescriptionEn(lesson) {
  return lessonCopyEn[lesson.id]?.description || buildLessonFallbackDescription(lesson);
}

export function localizeTool(tool, language) {
  if (!tool || language !== 'en') {
    return tool;
  }

  const copy = toolCopyEn[tool.id];

  if (!copy) {
    return tool;
  }

  return {
    ...tool,
    description: copy.description,
  };
}

export function localizeCase(caseItem, language) {
  if (!caseItem || language !== 'en') {
    return caseItem;
  }

  const copy = caseCopyEn[caseItem.id];

  if (!copy) {
    return caseItem;
  }

  return {
    ...caseItem,
    title: copy.title,
    description: copy.description,
  };
}

export function localizeCaseSections(sections = [], language) {
  if (language !== 'en') {
    return sections;
  }

  return sections.map((section) => {
    const copy = genericSectionEn[section.title];

    if (!copy) {
      return section;
    }

    return {
      ...section,
      title: copy.title,
      description: copy.description,
      points: [],
      prompts: section.prompts,
    };
  });
}

export function localizeLesson(lesson, language) {
  if (!lesson || language !== 'en') {
    return lesson;
  }

  return {
    ...lesson,
    title: getLessonTitleEn(lesson),
    description: getLessonDescriptionEn(lesson),
    audience: localizeLabel(lesson.audience, 'audience'),
    duration: localizeDuration(lesson.duration),
    category: localizeLabel(lesson.category, 'category'),
    tools: lesson.tools?.map((tool) => localizeLabel(tool, 'tools')),
    tags: lesson.tags?.map((tag) => localizeLabel(tag, 'tags')),
  };
}

export function localizeLearningContent(content, lesson, language) {
  if (!content || !lesson || language !== 'en') {
    return content;
  }

  const copy = lessonCopyEn[lesson.id];
  const firstVisual = content.sections?.find((section) => section.image);

  if (copy?.sections?.length) {
    return {
      ...content,
      title: getLessonTitleEn(lesson),
      hideLead: false,
      hideSourceNoticeLink: false,
      notice:
        copy.notice ||
        'This English version is a Uicoding reading note based on the original source. Visit the source for complete context.',
      sections: copy.sections.map((section, index) => ({
        ...section,
        image: section.image || (index === 0 ? firstVisual?.image : undefined),
        imageAlt: section.imageAlt || (index === 0 ? firstVisual?.imageAlt : undefined),
      })),
    };
  }

  const takeaways = copy?.takeaways ?? [
    'Understand the core workflow before copying commands or prompts.',
    'Apply the idea to a small real project so the result can be verified.',
    'Check the browser, build output, and final behavior before expanding scope.',
  ];

  return {
    ...content,
    hideLead: false,
    hideSourceNoticeLink: false,
    notice:
      'English mode shows a concise reading guide for this resource. Switch back to Chinese for the full Uicoding article.',
    sections: [
      {
        heading: 'Overview',
        blocks: [
          {
            type: 'paragraph',
            content: getLessonDescriptionEn(lesson),
          },
        ],
        image: content.sections?.find((section) => section.image)?.image,
        imageAlt: content.sections?.find((section) => section.image)?.imageAlt,
      },
      {
        heading: 'What to focus on',
        blocks: takeaways.map((item) => ({
          type: 'paragraph',
          content: item,
        })),
      },
      {
        heading: 'How to practice',
        blocks: [
          {
            type: 'paragraph',
            content:
              'Use this resource as a practical exercise: start with a small page or feature, write clear constraints, ask the AI coding tool to make one change at a time, and verify the result in the browser or build output.',
          },
          {
            type: 'paragraph',
            content:
              'If the original source is available, read it alongside this guide for complete context and the latest details.',
          },
        ],
      },
    ],
  };
}
